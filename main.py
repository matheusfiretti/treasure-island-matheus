print('Welcome to the Treasure Island')
lado = input('Você está em uma encruzilhada, escolha um lado para ir. Digite left ou right ')
jogando = True

while jogando:
    if lado == 'right':
        print('Game Over')
        jogando = False
    else:
        swim_wait = input('Você encontrou um lago. Existe uma ilha no meio do lago, digite "wait" para esperar um barco ou "swim" para nadar até a ilha ')

        if swim_wait == 'swim':
            print('Game Over')
            jogando = False

        else:
            door = input('Você chegou até a ilha. Existe uma casa com 3 portas de cores diferentes. Escolha red, blue ou yellow ')
            if door == 'red' or door == 'blue':
                print('Game over')
                jogando = False
            else:
                print('You win!')
                jogando = False

